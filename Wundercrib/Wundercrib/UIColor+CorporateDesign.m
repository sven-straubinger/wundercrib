    return [UIColor colorWithWhite:43.0/255.0 alpha:1.0];
}

+ (UIColor*)grayBoxFill
{
    return [UIColor colorWithWhite:185.0/255.0 alpha:1.0];
}

+ (UIColor*)grayBoxStroke
{
    return [UIColor colorWithWhite:160.0/255.0 alpha:1.0];
}

@end
